/**
 * @param {object[]} items
 * @param { Array< {k: string, v: any} >} excludes
 * @return {object[]}
 */
function excludeItems(items, excludes) {
  return items.filter((item) => excludes.every((ex) => item[ex.k] !== ex.v));
}
